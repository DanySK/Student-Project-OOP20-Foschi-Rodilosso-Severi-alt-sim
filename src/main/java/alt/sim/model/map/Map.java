package alt.sim.model.map;

public interface Map {

    /**
     * Gets map width.
     * @return width
     */
    int getWidth();

    /**
     * Gets map height.
     * @return height
     */
    int getHeight();

    /**
     * Gets X coordinate.
     * @return x coordinate
     */
    double getX();

    /**
     * Gets Y coordinate.
     * @return y coordinate
     */
    double getY();
}
